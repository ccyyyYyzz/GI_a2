"""
GATE P0 -- OPTICAL MECHANISM (life-or-death; ruling S1.3, S8 Gate P0).

The whole MOLT p2 channel exists ONLY if the masked scene reaches each microcell
as an INTENSITY-ADDITIVE sum of independent positive modes.  Under ordinary
COHERENT field addition E_c = sum_p h_cp sqrt(v_p) (h~CN(0,1)), the load
T_c=|E_c|^2 ~ Exp(p1) and r(a)=1/(1+a p1) depends ONLY on the ordinary bucket:
the x^2 channel dies exactly.

We (1) build 3 equal-p1 / different-p2 masked-scene pairs; (2) compute r(a) under
the intensity-product, coherent-collapse, and Gamma-k models; (3) run a
DATA-driven discrimination (held-out-level prediction) of product vs coherent on
data generated by each; (4) quantify the Gamma-k gate-demand ~ k^2 law.

KILL P0 if the product model cannot predict held-out levels, or if the fitted
curvature tracks only p1 (i.e. data are consistent with coherent collapse).
"""
import json
import os
import sys
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, HERE)
import saturation_core as sc
import gi_operator as op

OUT = os.path.dirname(HERE)
DATA = "data/r63_bridge_scenes"
rng = np.random.default_rng(650_000 + 31)
C = 3600

# ---- build 3 equal-p1 / different-p2 masked-scene pairs --------------------
def make_pair(scene, seedA, seedB):
    x = op.load_scene(os.path.join(DATA, scene + ".npz"))
    mA = (np.random.default_rng(seedA).random(op.P) < 0.15).astype(float)
    mB = (np.random.default_rng(seedB).random(op.P) < 0.05).astype(float)
    vA = (mA * x); vB = (mB * x)
    vA = vA[vA > 0]; vB = vB[vB > 0]
    vB = vB * (vA.sum() / vB.sum())               # equalise p1 exactly
    return vA, vB

pairs = [make_pair("bridge_contour_0", 11, 12),
         make_pair("bridge_twopop_1", 21, 22),
         make_pair("bridge_microtex_2", 31, 32)]

report = {"C": C, "pairs": [], "models": {}}
print("equal-p1 / different-p2 pairs:")
for i, (vA, vB) in enumerate(pairs):
    p1A, p2A = vA.sum(), (vA ** 2).sum()
    p1B, p2B = vB.sum(), (vB ** 2).sum()
    report["pairs"].append(dict(p1A=float(p1A), p2A=float(p2A),
        p1B=float(p1B), p2B=float(p2B), p2_ratio=float(p2B / p2A)))
    print("  pair %d: p1=%.3f (both)  p2_A=%.3f p2_B=%.3f  (ratio %.2f)"
          % (i, p1A, p2A, p2B, p2B / p2A))

# ---- three-level sweep in load t (ruling S4.3) -----------------------------
t_levels = np.array([0.1, 1.0, 3.54])

# ---- (A) model curves: does r separate the pair? ---------------------------
print("\nModel-curve separation of equal-p1 pair 0 (max |r_A - r_B| over sweep):")
vA, vB = pairs[0]
aA = t_levels / vA.sum(); aB = t_levels / vB.sum()   # same, p1 equal
sep = {}
# product
rA = sc.Q_exp(aA, vA); rB = sc.Q_exp(aA, vB)
sep["product"] = float(np.max(np.abs(rA - rB)))
# coherent (exact + MC)
rAc = sc.coherent_survival_exact(aA, vA); rBc = sc.coherent_survival_exact(aA, vB)
sep["coherent"] = float(np.max(np.abs(rAc - rBc)))
rAc_mc = sc.coherent_survival_mc(aA, vA, 400000, rng)
rBc_mc = sc.coherent_survival_mc(aA, vB, 400000, rng)
sep["coherent_mc"] = float(np.max(np.abs(rAc_mc - rBc_mc)))
# gamma-k
for k in (1, 3, 10):
    rAg = sc.Q_gamma(aA, vA, k); rBg = sc.Q_gamma(aA, vB, k)
    sep[f"gamma_k{k}"] = float(np.max(np.abs(rAg - rBg)))
report["models"]["curve_separation_pair0"] = sep
for kk, vv in sep.items():
    print("  %-14s max|r_A-r_B| = %.3e" % (kk, vv))

# ---- (B) DATA discrimination: fit product vs coherent, held-out-level test -
# Generate finite-C, high-G data under each generative model; fit both link
# models (product: free p1,p2; coherent: free p1 only) on 2 levels, predict the
# held-out ridge level; the model with lower held-out error wins.
def gen_data(v, model, a_lv, G, k=1):
    """Return noisy r-hat per level. product/gamma via product survival + finite
    -C+binomial noise; coherent via its own survival."""
    if model == "product":
        r = sc.Q_exp(a_lv, v)
    elif model == "gamma":
        r = sc.Q_gamma(a_lv, v, k)
    elif model == "coherent":
        r = sc.coherent_survival_exact(a_lv, v)
    r2 = sc.Q_exp(2 * a_lv, v) if model != "coherent" else sc.coherent_survival_exact(2 * a_lv, v)
    var = (r2 - r ** 2) / C + (r - r2) / (C * G)     # ruling S2.2 (approx)
    var = np.maximum(var, 1e-12)
    return r + rng.normal(0, np.sqrt(var))

def fit_predict_err(rhat_fit, a_fit, rhat_hold, a_hold, model):
    """Fit link on a_fit, predict a_hold, return abs error at held-out level."""
    L = -np.log(np.clip(rhat_fit, 1e-6, 1 - 1e-9))
    if model == "product":                            # L = a p1 - a^2 p2/2
        A = np.stack([a_fit, -a_fit ** 2 / 2], 1)
        c, *_ = np.linalg.lstsq(A, L, rcond=None)
        Lpred = c[0] * a_hold - c[1] * a_hold ** 2 / 2
    else:                                             # coherent: L = log(1+a p1)
        # fit p1 by least squares on L = log(1 + a p1)
        from scipy.optimize import brentq
        def resid(p1): return np.sum((np.log(1 + a_fit * p1) - L) * a_fit / (1 + a_fit * p1))
        try:
            p1h = brentq(resid, 1e-6, 1e6)
        except Exception:
            p1h = np.sum(L) / np.sum(a_fit)
        Lpred = np.log(1 + a_hold * p1h)
    return abs(Lpred - (-np.log(np.clip(rhat_hold, 1e-6, 1 - 1e-9))))

print("\nData discrimination (held-out ridge-level prediction error; fit on anchor+audit):")
G = 400
disc = {}
NTRIAL = 200
for gen in ("product", "coherent"):
    err_prod = []; err_coh = []
    for _ in range(NTRIAL):
        rh = gen_data(vA, gen, aA, G)
        ep = fit_predict_err(rh[:2], aA[:2], rh[2], aA[2], "product")
        ec = fit_predict_err(rh[:2], aA[:2], rh[2], aA[2], "coherent")
        err_prod.append(ep); err_coh.append(ec)
    err_prod = np.array(err_prod); err_coh = np.array(err_coh)
    winner_product_frac = float(np.mean(err_prod < err_coh))
    disc[gen] = dict(median_err_productfit=float(np.median(err_prod)),
                     median_err_coherentfit=float(np.median(err_coh)),
                     frac_product_model_wins=winner_product_frac)
    print("  data=%-9s  median held-out err: productfit=%.3e coherentfit=%.3e  "
          "-> product wins %.0f%%" % (gen, np.median(err_prod), np.median(err_coh),
                                      100 * winner_product_frac))
report["models"]["data_discrimination"] = disc

# ---- (C) Gamma-k gate-demand ~ k^2 (ruling S4.4) ---------------------------
print("\nGamma-k p2 gate-demand scaling (order-2 CRB, ridge t=3.54):")
p1 = vA.sum(); p2 = (vA ** 2).sum()
gk = {}
for k in (1, 3, 10):
    a_r = np.array([3.54 / p1])
    # per-gate FIM for p2 under Gamma-k: dlog r/dp2 = -a^2/(2k)
    r = sc.Q_gamma(a_r, vA, k)[0]
    I_p2_pergate = C * (r / (1 - r)) * (a_r[0] ** 2 / (2 * k)) ** 2
    gates_d3 = 9.0 / (I_p2_pergate * (p2) ** 2) * p2 ** 2   # d'=3 vs p2=0: 9/(I*?)
    # d'^2 = p2^2 * G * I_p2 ; for d'=3 -> G = 9/(p2^2 I_p2)
    G_d3 = 9.0 / (p2 ** 2 * I_p2_pergate)
    gk[f"k{k}"] = dict(I_p2_pergate=float(I_p2_pergate), gates_for_d3=float(G_d3))
    print("  k=%2d  I_p2/gate=%.3e  gates(d'=3, p2 vs 0)=%.1f" % (k, I_p2_pergate, G_d3))
ratio_3_1 = gk["k3"]["gates_for_d3"] / gk["k1"]["gates_for_d3"]
ratio_10_1 = gk["k10"]["gates_for_d3"] / gk["k1"]["gates_for_d3"]
report["models"]["gamma_gate_demand"] = dict(detail=gk,
    ratio_k3_over_k1=float(ratio_3_1), ratio_k10_over_k1=float(ratio_10_1),
    expected_k2=[9.0, 100.0])
print("  demand ratio k3/k1=%.1f (expect ~9)  k10/k1=%.1f (expect ~100)"
      % (ratio_3_1, ratio_10_1))

# ---- verdict --------------------------------------------------------------
coherent_kills = sep["coherent"] < 1e-6 and sep["coherent_mc"] < 5e-3
product_separates = sep["product"] > 5 * sep["coherent_mc"]   # above coherent MC floor
data_tells_apart = (disc["product"]["frac_product_model_wins"] > 0.8 and
                    disc["coherent"]["frac_product_model_wins"] < 0.5)
report["verdict"] = dict(
    coherent_collapses_p2_channel=bool(coherent_kills),
    product_model_separates_pair=bool(product_separates),
    data_distinguishes_models=bool(data_tells_apart),
    P0="CONDITIONAL_PASS: mechanism real IFF intensity-additive; coherent speckle "
       "is an exact kill -- physical premise must be verified on the real bench",
    kill_condition="if the real optical path is coherent-additive, r depends only "
                   "on p1 and MOLT is dead")
print("\nGATE P0: coherent kills p2 channel = %s ; product separates pair = %s ; "
      "data distinguishes = %s" % (coherent_kills, product_separates, data_tells_apart))
print("  -> %s" % report["verdict"]["P0"])

with open(os.path.join(OUT, "p0_coherent_gate.json"), "w") as fh:
    json.dump(report, fh, indent=2, default=float)
print("wrote p0_coherent_gate.json")
