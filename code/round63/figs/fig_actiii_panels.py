RHO_CURVE_MAX = 30.0     # frozen exact-PMF evaluation is smooth to here for
                         # BOTH anchors (nu=200 finite-difference noise sets in
                         # near the count ceiling at rho ~ 31; nu=2000 later)


def _ridge_curve_exact(nu, rho_lo=0.3, rho_hi=RHO_CURVE_MAX, n=130):
    """Dense (rho, J) from the FROZEN exact count-law machinery
    (code/round63/physics.fisher_exact), information about log-lambda per
    unit time in tau units — same definition as fisher_ridge.py. Evaluated
    densely instead of reading the coarse 49-point cache, whose nu=200 tail
    (rho >= 44) carries a visible finite-difference artifact."""
    sys.path.insert(0, R63)
    from physics import fisher_exact
    T = float(nu)
    rho = np.geomspace(rho_lo, rho_hi, n)
    with np.errstate(all="ignore"):
        ie = np.array([fisher_exact(r, T, 1.0) * r ** 2 / T for r in rho])
    return rho, ie


def _mech_scene_insets():
    """(scene, recon) 32x32 arrays for the pipeline object/recon boxes: the
    m1_maze_1 confirmatory scene and its RIDGE-SCAT32 seed-0 reconstruction
    from the panel-(d) cache; deterministic synthetic fallback if absent."""
    if os.path.exists(D_NPZ):
        z = np.load(D_NPZ, allow_pickle=True)
        return z["max__gt"], z["max__RIDGE-SCAT32__recon"]
    rng = np.random.default_rng(1)                      # fallback (display)
    s = (rng.random((32, 32)) < 0.15).astype(float)
    return s, s + 0.05 * rng.standard_normal((32, 32))


