def _dot_dQ():
    """Per-image dQ (5-seed mean PSNR_rad, RIDGE-SCAT32 - SCAT32-060, nu=2000)
    grouped by family, from the imaging shards."""
    sys.path.insert(0, R63)
    sys.path.insert(0, os.path.dirname(R63))
    import m1_runner as m1
    acc = {}
    for p in sorted(glob.glob(os.path.join(SHARDS, "*.csv"))):
        if os.path.basename(p).startswith("M1_CERT"):
            continue
        for r in _read_cert_csv(p):     # generic csv (no overflow here)
            if _fnum(r["nu"]) != 2000.0:
                continue
            sid = r["shard_id"]
            arm = sid[len("M1_"):sid.rindex("_")]
            if arm in ("RIDGE-SCAT32", "SCAT32-060"):
                acc.setdefault((arm, r["image"]), []).append(
                    _fnum(r["PSNR_rad"]))
    out = {}
    imgs = sorted({img for (_a, img) in acc})
    for img in imgs:
        ri = acc.get(("RIDGE-SCAT32", img), [])
        qi = acc.get(("SCAT32-060", img), [])
        if len(ri) == 5 and len(qi) == 5:
            out[img] = (float(np.mean(ri)) - float(np.mean(qi)),
                        m1._family_of(img))
    return out


