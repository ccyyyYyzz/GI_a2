def panel_b():
    path = os.path.join(M1, "R18_GAP_PROBE_REPLICATION.md")
    hdr, rows = _read_md_table(
        path, ["scene", "nu", "b", "DOSE_ONLY_PRIMAL_GAP_PER_R"])
    ig = hdr.index("DOSE_ONLY_PRIMAL_GAP_PER_R")
    isc, inu, ib = hdr.index("scene"), hdr.index("nu"), hdr.index("b")
    data = {}                              # family -> {anchor: gap}
    for r in rows:
        fam = r[isc].replace("m1_dev_", "")
        nu, b = _fnum(r[inu]), _fnum(r[ib])
        anchor = "A" if (nu == 2000.0 and b == 0.60) else "B"
        data.setdefault(fam, {})[anchor] = _fnum(r[ig])
    fams = [f for f in FAM_ORDER if f in data]

    fig, ax = plt.subplots(figsize=(3.9, 3.0))
    x = np.arange(len(fams))
    gA = [data[f]["A"] for f in fams]
    gB = [data[f]["B"] for f in fams]
    # paired stems + markers
    for xi, a, bb in zip(x, gA, gB):
        ax.plot([xi, xi], [min(a, bb), max(a, bb)], color=GREY, lw=1.0,
                zorder=1)
    ax.plot(x, gA, "o", color=BLUE, ms=7, zorder=3,
            label=r"anchor A: $\nu{=}2000,\, b{=}0.60$")
    ax.plot(x, gB, "D", color=ORANGE, ms=6, zorder=3,
            label=r"anchor B: $\nu{=}200,\, b{=}0.05$")
    ax.axhline(0.0, color="black", lw=0.6)
    ax.set_xticks(x)
    ax.set_xticklabels(fams, rotation=30, ha="right", fontsize=8)
    ax.set_ylabel(r"dose-only primal gap / $r$   ($\log\det$ per dim.)")
    ax.set_ylim(0.0, 1.05)
    ax.set_xlim(-0.5, len(fams) - 0.5)
    ax.set_title("(b) Dose-only geometry headroom is real\n"
                 "(development-only lower bound; no gate)", fontsize=9)
    # right axis: D-efficiency lower bound = exp(gap)
    sec = ax.secondary_yaxis("right", functions=(np.exp, np.log))
    sec.set_ylabel(r"$D$-efficiency lower bound  $\exp(\mathrm{gap})$")
    sec.set_yticks([1.0, 1.5, 2.0, 2.5])
